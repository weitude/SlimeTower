def createcode(l, x, y, type, k):
    if type == 0:
        print(f"let map[{l * 16 * 24 + x * 24 + y}] = {0};")
    elif type == 1:
        print(f"let map[{l * 16 * 24 + x * 24 + y}] = {1000};")
    elif type == 2:
        t = random.randint(0, 2)
        print(f"let map[{l * 16 * 24 + x * 24 + y}] = {2000 + t * 100 + k[t]};")
        return t
    elif type == 3:
        print(f"let map[{l * 16 * 24 + x * 24 + y}] = {3000 + random.randint(0, 2)};")
    elif type == 4:
        print(f"let map[{l * 16 * 24 + x * 24 + y}] = {4001};")
    elif type == 5:
        print(f"let map[{l * 16 * 24 + x * 24 + y}] = {4000};")
    elif type == 6:
        print(f"let map[{l * 16 * 24 + x * 24 + y}] = {5000};")

    return 0


